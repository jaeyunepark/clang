#include <iostream>

using namespace std;

int main() {

	char secret_code = 'h';
	cout << "비밀 코드를 맞춰 보세요:";
	char code;
	cin >> code;

	if (code < secret_code) {
		cout << code << "뒤에 있습니다" << endl;
	}
	else if(code > secret_code) {
		cout << code << "앞에 있습니다" << endl;
	}
	else {
		cout << code << "맞추었습니다!" << endl;
	}


	return 0;
}